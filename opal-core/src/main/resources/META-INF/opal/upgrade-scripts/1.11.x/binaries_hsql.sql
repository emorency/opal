CREATE TABLE VALUE_SET_BINARY_VALUE (
  ID           BIGINT GENERATED BY DEFAULT AS IDENTITY (
  START WITH 1 )       NOT NULL PRIMARY KEY,
  OCCURRENCE   INTEGER NOT NULL,
  SIZE         INTEGER NOT NULL,
  VALUE        VARBINARY(16777216),
  VALUE_SET_ID BIGINT  NOT NULL,
  VARIABLE_ID  BIGINT  NOT NULL,
  UNIQUE (VALUE_SET_ID, VARIABLE_ID, OCCURRENCE),
  FOREIGN KEY (VALUE_SET_ID) REFERENCES VALUE_SET (ID),
  FOREIGN KEY (VARIABLE_ID) REFERENCES VARIABLE (ID)
);
CREATE INDEX OCCURRENCEINDEX ON VALUE_SET_BINARY_VALUE (OCCURRENCE);
